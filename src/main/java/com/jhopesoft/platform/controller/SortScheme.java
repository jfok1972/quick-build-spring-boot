package com.jhopesoft.platform.controller;

import javax.annotation.Resource;
import javax.servlet.http.HttpServletRequest;
import org.springframework.web.bind.annotation.RestController;
import org.springframework.web.bind.annotation.RequestMapping;


import com.alibaba.fastjson.JSONArray;
import com.alibaba.fastjson.JSONObject;
import com.jhopesoft.framework.bean.ActionResult;
import com.jhopesoft.platform.service.SortSchemeService;

/**
 * 
 * @author jiangfeng
 *
 */
@RestController
@RequestMapping("/platform/scheme/sort")
public class SortScheme {

	@Resource
	private SortSchemeService sortSchemeService;

	/**
	 * 保存用户设置的 列表 方案
	 * 
	 * @param request
	 * @param sortSchemeId
	 * @param schemeDefine
	 *          传进来的所有column包括合并列头的定义
	 * @return
	 */

	@RequestMapping("/updatedetails.do")
	public  ActionResult updateSortSchemeDetails(HttpServletRequest request, String dataObjectId,
			String sortSchemeId, String schemeDefine, String sortSchemeName, Boolean mydefault, Boolean shareowner,
			Boolean shareall) {
		ActionResult result = null;
		try {
			result = sortSchemeService.updateSortSchemeDetails(request, dataObjectId, sortSchemeId, sortSchemeName,
					schemeDefine, mydefault, shareowner, shareall);
		} catch (Exception e) {
			e.printStackTrace();
		}
		return result;
	}

	@RequestMapping("/getdetails.do")
	public  JSONObject getSortSchemeDetails(HttpServletRequest request, String sortSchemeId) {
		return sortSchemeService.getSortSchemeDetails(request, sortSchemeId);
	}

	@RequestMapping("/deletescheme.do")
	public  ActionResult deleteSortScheme(HttpServletRequest request, String schemeid) {
		return sortSchemeService.deleteSortScheme(request, schemeid);
	}

	/**
	 * 检查用户录入名称的时候，是不是有重复的
	 * 
	 * @param request
	 * @param type
	 *          类型 列表名称 ，form名称 ， 筛选方案名称， 视图方案名称
	 * @param name
	 *          要检查的名称
	 * @param id
	 *          修改的话，id
	 * @return
	 * @author jiangfeng
	 */
	@RequestMapping("/checknamevalidate.do")
	public  ActionResult checkNameValidate(String name, String id) {
		return sortSchemeService.checkNameValidate(name, id);
	}

	/**
	 * 实体对象的默认排序的读取和设置
	 * 
	 * @param dataObjectId
	 * @return
	 */
	@RequestMapping("/getdefaultdetails.do")
	public  JSONArray getSortDefaultDetails(String dataObjectId) {
		return sortSchemeService.getSortDefaultDetails(dataObjectId);
	}

	@RequestMapping("/updatedefaultdetails.do")
	public  ActionResult updateDefaultSortDetails(String dataObjectId, String schemeDefine) {
		ActionResult result = null;
		try {
			result = sortSchemeService.updateDefaultSortDetails(dataObjectId, schemeDefine);
		} catch (Exception e) {
			e.printStackTrace();
		}
		return result;
	}
}
